    <script type="text/javascript">
        $(window).scroll(function(event) {
            var y = $(this).scrollTop();
            if (y >= 1) {
                $('#navbar').removeClass('navbar');
                $('#navbar').addClass('navbarscroll');
                $('.linknav').removeClass('link');
                $('.linknav').addClass('linkscroll');
                $('#logo1').removeClass('logo');
                $('#logo1').addClass('logoscroll');
            } else {
                $('#navbar').removeClass('navbarscroll');
                $('#navbar').addClass('navbar');
                $('#logo1').removeClass('logoscroll');
                $('#logo1').addClass('logo');
                $('.linknav').removeClass('linkscroll');
                $('.linknav').addClass('link');

            }
        });
    </script>
